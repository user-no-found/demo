//!WebSocket 配置模块
//!
//!统一管理 WebSocket 通信相关的所有配置项。

//========================================
//客户端配置
//========================================

///连接超时时间（秒）
pub const CONNECT_TIMEOUT_SECS: u64 = 10;

///读取超时时间（秒），0 表示无超时
pub const READ_TIMEOUT_SECS: u64 = 0;

///写入超时时间（秒），0 表示无超时
pub const WRITE_TIMEOUT_SECS: u64 = 0;

